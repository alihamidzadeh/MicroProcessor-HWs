uint8_t password[]={'p','a','s','s','1','2','3'};

int getString(char string[], uint32_t to){

    char substring[1000];
    uint32_t c = 0;
    while (c <= to - 2 ){
        substring[c] = string[2 + c - 1];
        c++;
    }
    substring[c]='\0';


	if (strcmp(substring, password)){
		return 1;
	}

    return 0;
}

uint32_t time;
uint32_t pir_active = 0; //0,1,2
